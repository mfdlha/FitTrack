    private void tombolPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolPilihActionPerformed
        // TODO add your handling code here:
        int baris = tabelPaket.getSelectedRow();
        int masaAktif = Integer.parseInt(tabelPaket.getValueAt(baris, 3).toString().replace(" Hari", ""));
        
        java.util.Calendar cal = java.util.Calendar.getInstance();
        cal.add(java.util.Calendar.DAY_OF_YEAR, masaAktif);

        java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy-MM-dd");
        String tglExp = sdf.format(cal.getTime());

        FormTransaksi.fieldTanggalEXP.setText(tglExp);
        
            if (baris == -1) return;

            String idPaket = tabelPaket.getValueAt(baris, 0).toString();
            String namaPaket = tabelPaket.getValueAt(baris, 1).toString();
            double hargaDouble = Double.parseDouble(tabelPaket.getValueAt(baris, 2).toString());
            java.text.DecimalFormat df = new java.text.DecimalFormat("#"); 
            String hargaBersih = df.format(hargaDouble);
            
            FormTransaksi.fieldIdPaket.setText(idPaket);
            FormTransaksi.fieldNamaPaket.setText(namaPaket);
            FormTransaksi.fieldHargaPaket.setText(hargaBersih);
            FormTransaksi.formatTotalBayar(hargaDouble); 

        this.dispose();
        
    }//GEN-LAST:event_tombolPilihActionPerformed

