    private void tampilData(String keyword) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Paket");
        model.addColumn("Nama Paket");
        model.addColumn("Harga");
        model.addColumn("Masa Aktif");

        try {
            Dao.PaketDAO dao = new Dao.PaketDAOImpl();
            List<Model.Paket> listPaket = dao.getByName(keyword); // Gunakan method yang sudah ada

            for (Model.Paket p : listPaket) {
                model.addRow(new Object[]{
                    p.getIdPaket(),
                    p.getNamaPaket(),
                    p.getHarga(),
                    p.getMasaAktif() + " Hari"
                });
            }
            tabelPaket.setModel(model);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

