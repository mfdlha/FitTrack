    private void tampilData(String keyword) {
        DefaultTableModel model = new DefaultTableModel();
        
        model.addColumn("ID");
        model.addColumn("Nama");
        model.addColumn("No HP");
        model.addColumn("Status");

        try {
            MemberDAO dao = new MemberDAOImpl();
            List<Member> listMember = dao.getNonActiveMember(keyword);

            for (Member m : listMember) {
                model.addRow(new Object[]{
                    m.getIdMember(),
                    m.getNama(),
                    m.getNoHp(),
                    m.getStatus()
                });
            }
            tabelMember.setModel(model);
            tabelMember.getColumnModel().getColumn(0).setPreferredWidth(25);
            tabelMember.getColumnModel().getColumn(1).setPreferredWidth(100);
            tabelMember.getColumnModel().getColumn(2).setPreferredWidth(100);
            tabelMember.getColumnModel().getColumn(3).setPreferredWidth(100);

        } catch (Exception e) {
            System.out.println("Error tampil data dari DAO: " + e.getMessage());
        }
    }

