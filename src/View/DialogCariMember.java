    private void tombolPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolPilihActionPerformed
        // TODO add your handling code here:
        int baris = tabelMember.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Silakan klik data member pada tabel terlebih dahulu!");
            return;
        }

        String idMember = tabelMember.getValueAt(baris, 0).toString();
        String namaMember = tabelMember.getValueAt(baris, 1).toString();

        FormTransaksi.fieldIdMember.setText(idMember);
        FormTransaksi.fieldNamaMember.setText(namaMember);

        this.dispose();
    }//GEN-LAST:event_tombolPilihActionPerformed

