    private void tampilData() {
        javax.swing.table.DefaultTableModel model = new javax.swing.table.DefaultTableModel();
        model.addColumn("ID Kelas"); 
        model.addColumn("Nama Kelas");
        model.addColumn("Instruktur");
        model.addColumn("Hari");
        model.addColumn("Jam Mulai");
        model.addColumn("Jam Selesai");
        model.addColumn("Kapasitas");

        try {
            Dao.KelasDAO dao = new Dao.KelasDAOImpl();
            java.util.List<Model.Kelas> listKelas = dao.getAll("");

            for (Model.Kelas k : listKelas) {
                String jamMulai = k.getJamMulai();
                String jamSelesai = k.getJamSelesai();
                
                if (jamMulai != null && jamMulai.length() >= 5) {
                    jamMulai = jamMulai.substring(0, 5);
                }
                if (jamSelesai != null && jamSelesai.length() >= 5) {
                    jamSelesai = jamSelesai.substring(0, 5);
                }
                
                model.addRow(new Object[]{
                    k.getIdKelas(),
                    k.getNamaKelas(),
                    k.getInstruktur(),
                    k.getHari(),
                    jamMulai,      
                    jamSelesai,    
                    k.getKapasitas()
                });
            }
            tabel.setModel(model);

        } catch (Exception e) {
            System.err.println("Gagal memuat data tabel: " + e.getMessage());
        }
    }
    
