    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        // TODO add your handling code here:
        try {
            
            String namaKelas = fieldNamaKelas.getText();
            String instruktur = fieldInstruktur.getText();
            String hari = boxHari.getSelectedItem().toString(); 

            SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");
            String jamMulai = sdf.format(spinnerJamMulai.getValue());
            String jamSelesai = sdf.format(spinnerJamSelesai.getValue());

            int kapasitas = (Integer) spinnerKapasitas.getValue();

            if (namaKelas.trim().isEmpty() || instruktur.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Kelas dan Instruktur tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }
            
            Kelas kelasBaru = new Kelas(namaKelas, instruktur, hari, jamMulai, jamSelesai, kapasitas);

            KelasDAO dao = new KelasDAOImpl();
            boolean sukses = dao.insert(kelasBaru);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data Kelas berhasil disimpan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            tampilData();
            resetField();

            } else {
                JOptionPane.showMessageDialog(this, "Gagal menyimpan data kelas.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error System", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tombolSimpanActionPerformed

    private void tombolEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolEditActionPerformed
        // TODO add your handling code here:
        try {
            int baris = tabel.getSelectedRow();

            if (baris == -1) {
                JOptionPane.showMessageDialog(this, "Pilih data di tabel terlebih dahulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            int idKelas = Integer.parseInt(tabel.getValueAt(baris, 0).toString());
            String namaKelas = fieldNamaKelas.getText();
            String instruktur = fieldInstruktur.getText();
            String hari = boxHari.getSelectedItem().toString();

            SimpleDateFormat sdf = new SimpleDateFormat("HH:mm");
            String jamMulai = sdf.format(spinnerJamMulai.getValue());
            String jamSelesai = sdf.format(spinnerJamSelesai.getValue());

            int kapasitas = (Integer) spinnerKapasitas.getValue();

            if (namaKelas.trim().isEmpty() || instruktur.trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama Kelas dan Instruktur tidak boleh kosong!", "Peringatan", JOptionPane.WARNING_MESSAGE);
                return;
            }

            Kelas kelasEdit = new Kelas(idKelas, namaKelas, instruktur, hari, jamMulai, jamSelesai, kapasitas);

            KelasDAO dao = new KelasDAOImpl();
            boolean sukses = dao.update(kelasEdit);

            if (sukses) {
                JOptionPane.showMessageDialog(this, "Data Kelas berhasil diubah!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
                tampilData();
                resetField();
            } else {
                JOptionPane.showMessageDialog(this, "Gagal mengubah data kelas.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Terjadi kesalahan: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tombolEditActionPerformed

    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        try {
            int baris = tabel.getSelectedRow();

            if (baris != -1) {
                fieldNamaKelas.setText(tabel.getValueAt(baris, 1).toString());
                fieldInstruktur.setText(tabel.getValueAt(baris, 2).toString());
                boxHari.setSelectedItem(tabel.getValueAt(baris, 3).toString());

                java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("HH:mm");

                java.util.Date waktuMulai = sdf.parse(tabel.getValueAt(baris, 4).toString());
                spinnerJamMulai.setValue(waktuMulai);

                java.util.Date waktuSelesai = sdf.parse(tabel.getValueAt(baris, 5).toString());
                spinnerJamSelesai.setValue(waktuSelesai);

                int kapasitas = Integer.parseInt(tabel.getValueAt(baris, 6).toString());
                spinnerKapasitas.setValue(kapasitas);
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error memuat data: " + e.getMessage(), "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_tabelMouseClicked

