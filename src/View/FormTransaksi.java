    private void tombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolSimpanActionPerformed
        // TODO add your handling code here:
        if (fieldIdMember.getText().isEmpty() || fieldIdPaket.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan pilih Member dan Paket terlebih dahulu!");
            return;
        }

        try {
            Model.Transaksi t = new Model.Transaksi();
            t.setIdMember(Integer.parseInt(fieldIdMember.getText()));
            t.setIdPaket(Integer.parseInt(fieldIdPaket.getText()));
            t.setTglBayar(txtTanggal.getText()); // Pastikan format YYYY-MM-DD
            t.setTglExp(fieldTanggalEXP.getText());

            String totalMentah = fieldTotalBayar.getText().replaceAll("[^0-9]", "");
            t.setTotalBayar(Double.parseDouble(totalMentah));

            t.setStatusBayar(boxStatusBayar.getSelectedItem().toString());

            dao.insert(t);

            JOptionPane.showMessageDialog(this, "Data Transaksi Berhasil Disimpan!");

            resetField();
            tampilData();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal Simpan: " + e.getMessage());
    }
    }//GEN-LAST:event_tombolSimpanActionPerformed

