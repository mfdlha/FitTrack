    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        // TODO add your handling code here:
        int baris = tabel.getSelectedRow();
            tombolCariMember.setEnabled(false); 
            tombolCariPaket.setEnabled(false);
            fieldIdMember.setEditable(false);
            fieldIdPaket.setEditable(false);

            idTransaksiTerpilih = tabel.getValueAt(baris, 0).toString();

            fieldIdMember.setText(tabel.getValueAt(baris, 1).toString());     
            fieldIdPaket.setText(tabel.getValueAt(baris, 2).toString());       
            fieldNamaMember.setText(tabel.getValueAt(baris, 3).toString());   
            fieldNamaPaket.setText(tabel.getValueAt(baris, 4).toString());    

            String hargaRaw = tabel.getValueAt(baris, 5).toString();

            String hargaBersih = hargaRaw.replaceAll("[^0-9]", "");

            fieldHargaPaket.setText(hargaBersih); 

            double total = Double.parseDouble(hargaBersih);
            formatTotalBayar(total);

            fieldTanggalEXP.setText(tabel.getValueAt(baris, 7).toString());
            boxStatusBayar.setSelectedItem(tabel.getValueAt(baris, 8).toString());
            
            
    }//GEN-LAST:event_tabelMouseClicked

    private void tombolEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolEditActionPerformed
        // TODO add your handling code here:
        if (idTransaksiTerpilih == null || idTransaksiTerpilih.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silakan pilih data dari tabel terlebih dahulu!");
            return;
        }

        try {
            String statusBaru = boxStatusBayar.getSelectedItem().toString();
            dao.updateStatus(Integer.parseInt(idTransaksiTerpilih), statusBaru);

            JOptionPane.showMessageDialog(this, "Status berhasil diupdate!");
            tampilData();
            resetField();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengupdate status: " + e.getMessage());
        }
    }//GEN-LAST:event_tombolEditActionPerformed

    private void tombolCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tombolCariActionPerformed
        // TODO add your handling code here:
        String keyword = fieldCariTransaksi.getText();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("ID Transaksi");
        model.addColumn("ID Member");
        model.addColumn("ID Paket");
        model.addColumn("Member");
        model.addColumn("Paket");
        model.addColumn("Total");
        model.addColumn("Tgl Bayar");
        model.addColumn("Tgl Exp");
        model.addColumn("Status");

        java.text.NumberFormat formatRupiah = java.text.NumberFormat.getCurrencyInstance(new java.util.Locale("id", "ID"));

        for (Model.Transaksi t : dao.cari(keyword)) {
            model.addRow(new Object[]{
                t.getIdTransaksi(), 
                t.getIdMember(),
                t.getIdPaket(),
                t.getNamaMember(),
                t.getNamaPaket(),
                formatRupiah.format(t.getTotalBayar()),
                t.getTglBayar(),
                t.getTglExp(),
                t.getStatusBayar()
            });
        }
        tabel.setModel(model);
    }//GEN-LAST:event_tombolCariActionPerformed

